    public void BuscarObs(Mensalidade m){
        mensalidade = m;
        
        if(m.getObservacao() != null){
            memoObs.setText(m.getObservacao());
            caracteresRestantes = 255 - memoObs.getText().length();
            lblCaracteres.setText(caracteresRestantes + " caracteres restantes.");
        } else{
            memoObs.setText("");
        }
        
        memoObs.requestFocusInWindow();
    }

