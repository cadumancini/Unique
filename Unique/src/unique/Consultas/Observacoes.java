    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed
        // TODO add your handling code here:
        conexao = HibernateUtil.openSession();
        tx = conexao.beginTransaction();
        
        mensalidade.setObservacao(memoObs.getText());
        
        boolean funcionou = true;
        
        try{
            conexao.saveOrUpdate(mensalidade);
            tx.commit();
        } catch(Exception e){
            funcionou = false;
            JOptionPane.showMessageDialog(this, "Erro ao salvar observação. Motivo: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
        
        conexao.close();
        
        if(funcionou){
            JOptionPane.showMessageDialog(this, "Observação salva com sucesso. Pressione OK para Sair", "Erro", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        }
        
    }//GEN-LAST:event_btnOkActionPerformed

