    private void formWindowClosed(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosed
        //Não existe. Criando as configurações Default:
        Config configuracoes = new Config();

        //configuracoes.setID(1L);
        configuracoes.setID(1L);
        configuracoes.setDiaLimiteDescontoMaior(Integer.parseInt(txtBoxDPDia.getText()));
        configuracoes.setDescontoMaior(Long.parseLong(txtBoxDPValor.getText()));
        configuracoes.setDiaLimiteDescontoMenor(Integer.parseInt(txtBoxDSDia.getText()));
        configuracoes.setDescontoMenor(Long.parseLong(txtBoxDSValor.getText()));
        configuracoes.setDescontoMaiorEnbled(checkDPAtivo.isSelected());
        configuracoes.setDescontoMenorEnbled(checkDSAtivo.isSelected());

        //Salvando no banco:
        conexao = HibernateUtil.openSession();
        Transaction tx = conexao.beginTransaction();
        conexao.saveOrUpdate(configuracoes);
        
        try{
            conexao.save(configuracoes);
            tx.commit();
        } catch(Exception e){
                JOptionPane.showMessageDialog(this, "Operação mal sucedida. Motivo: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
        
        conexao.close();
        
        instance = null;
    }//GEN-LAST:event_formWindowClosed

