    private Configuracoes() {
        initComponents();
        
        setIconImage(Toolkit.getDefaultToolkit().getImage(getClass().getResource("Logo.png")));
        //Temos algum registro na tabela?
        conexao = HibernateUtil.openSession();
        Criteria select = conexao.createCriteria(Config.class);
        select.add(Restrictions.eq("ID", 1L));

        if(select.list().size() <= 0){
            //Não existe. Criando as configurações Default:
            Config configuracoes = new Config();
            
            configuracoes.setID(1L);
            configuracoes.setDiaLimiteDescontoMaior(5);
            configuracoes.setDescontoMaior(15L);
            configuracoes.setDiaLimiteDescontoMenor(10);
            configuracoes.setDescontoMenor(10L);
            configuracoes.setDescontoMaiorEnbled(true);
            configuracoes.setDescontoMenorEnbled(true);
            
            //Salvando no banco:
            Transaction tx = conexao.beginTransaction();
            
            try{
                conexao.save(configuracoes);
                tx.commit();
            } catch(Exception e){
                JOptionPane.showMessageDialog(this, "Operação mal sucedida. Motivo: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }
        
        select = conexao.createCriteria(Config.class);
        select.add(Restrictions.eq("ID", 1L));
        
        //Existe. Carregando as configurações:
        Config configuracoes = (Config)select.list().get(0);

        txtBoxDPDia.setText(String.valueOf(configuracoes.getDiaLimiteDescontoMaior()));
        txtBoxDPValor.setText(String.valueOf(configuracoes.getDescontoMaior()));

        txtBoxDSDia.setText(String.valueOf(configuracoes.getDiaLimiteDescontoMenor()));
        txtBoxDSValor.setText(String.valueOf(configuracoes.getDescontoMenor()));

        checkDPAtivo.setSelected(configuracoes.isDescontoMaiorEnbled());
        checkDSAtivo.setSelected(configuracoes.isDescontoMenorEnbled());
        
        conexao.close();
        
        checkDPAtivoActionPerformed(null);
        checkDSAtivoActionPerformed(null);
    }
    
